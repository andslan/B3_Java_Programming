package day04_variables;

public class FloatingNumbers {

    public static void main(String[] args) {
        //in java you cannot store fractions. --- > 3/4
        System.out.println(3 / 4); // 0.75

        // how do we store decimal numbers: 4.5 or 5.99
        double price = 3.99;

        float average = 0.19f;

        System.out.println(price);          // datatype is double
        System.out.println(average);        // datatype is float
        System.out.println(4.5);            // default datatype is double


    }
}
